// Operações aritméticas

// Operador Aritmético	Descrição
// +  	Adição
// -	Subtração
// *	Multiplicação
// /	Divisão
// %	Módulo (resto de uma operação de divisão)
// +=	Incremento
// -=	Decremento


console.log(10 + 22);
console.log(20 - 10);
console.log(4 * 2);
console.log(4 / 2);

let number = 20;
number += 1;
console.log(number)

number -= 1;
console.log(number)

